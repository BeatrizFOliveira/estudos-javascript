//OPERADORES ARITMÉTICOS
/*
Adição +
Subtração -
Multiplicação *
Divisão /
Resto %
Potencia **
*/

console.log(2*(4+5)/(4-1**3));

//OPERADORES DE COMPARAÇÃO
/*
Igual ==
Igualdade estrita ===
Diferente !=
Diferença estrita !==
Maior >
Menor <
Maior ou igual >=
Menor ou igual <=
*/

console.log(3>3);
console.log(4 <= 10);
console.log(4 === "4");

//OPERADORES LÓGICOS
/*
E &&
Ou ||
Não !
*/

console.log(10 >= 9 && 5 < 4);
console.log(10 >= 9 || 5 < 4);
console.log(!1 == 0);
